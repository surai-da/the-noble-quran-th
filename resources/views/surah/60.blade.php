  <p class="text text--grey text--medium text--regular translation">
    O believers! Do not take My enemies and yours as trusted allies, showing them affection even though they deny what has come to you of the truth. They drove the Messenger and yourselves out ˹of Mecca˺, simply for your belief in Allah, your Lord. If you ˹truly˺ emigrated<sup foot_note=77510>1</sup> to struggle in My cause and seek My pleasure, ˹then do not take them as allies,˺ disclosing secrets ˹of the believers˺ to the pagans out of affection for them, when I know best whatever you conceal and whatever you reveal. And whoever of you does this has truly strayed from the Right Way.
  </p>
  <p class="text text--green text--medium text--regular english">
    — Dr. Mustafa Khattab, the Clear Quran
  </p>
