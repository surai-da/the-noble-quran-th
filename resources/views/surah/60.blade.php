  <p class="text text--grey text--medium text--regular translation">
    O you who have believed, when the believing women come to you as emigrants, examine [i.e., test] them. Allah is most knowing as to their faith. And if you know them to be believers, then do not return them to the disbelievers; they are not lawful [wives] for them, nor are they lawful [husbands] for them. But give them [i.e., the disbelievers] what they have spent.<sup foot_note=130752>1</sup> And there is no blame upon you if you marry them when you have given them their due compensation [i.e., mahr]. And hold not to marriage bonds with disbelieving women, but ask for what you have spent and let them [i.e., the disbelievers] ask for what they have spent.<sup foot_note=130753>2</sup> That is the judgement of Allah; He judges between you. And Allah is Knowing and Wise.
  </p>
