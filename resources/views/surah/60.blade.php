  <p class="text text--grey text--medium text--regular translation">
    O believers! When the believing women come to you as emigrants,<sup foot_note=77511>1</sup> test their intentions—their faith is best known to Allah—and if you find them to be believers, then do not send them back to the disbelievers. These ˹women˺ are not lawful ˹wives˺ for the disbelievers, nor are the disbelievers lawful ˹husbands˺ for them. ˹But˺ repay the disbelievers whatever ˹dowries˺ they had paid. And there is no blame on you if you marry these ˹women˺ as long as you pay them their dowries. And do not hold on to marriage with polytheistic women.<sup foot_note=77512>2</sup> ˹But˺ demand ˹repayment of˺ whatever ˹dowries˺ you had paid, and let the disbelievers do the same. That is the judgment of Allah—He judges between you. And Allah is All-Knowing, All-Wise.
  </p>
  <p class="text text--green text--medium text--regular english">
    — Dr. Mustafa Khattab, the Clear Quran
  </p>
