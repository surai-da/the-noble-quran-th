  <p class="text text--grey text--medium text--regular translation">
    O Prophet! ˹Instruct the believers:˺ When you ˹intend to˺ divorce women, then divorce them with concern for their waiting period,<sup foot_note=77520>1</sup> and count it accurately. And fear Allah, your Lord. Do not force them out of their homes, nor should they leave—unless they commit a blatant misconduct. These are the limits set by Allah. And whoever transgresses Allah’s limits has truly wronged his own soul. You never know, perhaps Allah will bring about a change ˹of heart˺ later.<sup foot_note=77521>2</sup> 
  </p>
  <p class="text text--green text--medium text--regular english">
    — Dr. Mustafa Khattab, the Clear Quran
  </p>
